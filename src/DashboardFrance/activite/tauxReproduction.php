<h3>Taux de reproduction R effectif</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Un paramètre important est R, le taux de reproduction du virus. Si R est inférieur à 1, l'épidémie régresse, car un malade contamine en moyenne moins d'1 personne.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Il est très difficile d'évaluer précisément ce paramètre. Le gouvernement ne détaille pas sa méthode. Personnellement je me suis basé sur la méthode allemande, que j'ai modifiée pour que cela colle avec les résultats de notre gouvernement.</p>
<!-- /wp:paragraph -->

