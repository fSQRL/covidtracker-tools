<h3>Carte d'incidence</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Cette carte présente l'incidence, c'est-à-dire le nombre de tests positifs sur une semaine dans chaque département, ramené à 100 000 habitants. Un département est considéré comme rouge si son incidence dépasse 50 cas pour 100k habitants.</p>
