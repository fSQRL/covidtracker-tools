<h2 style="margin-top : 80px;">Quand pourrez-vous vous faire vacciner ?</h2>
Le gouvernement souhaite vacciner 1 million de personnes en janvier 2021, 12 à 14 millions d'ici à mi-avril
et 15 à 27 millions d'ici à juillet.

<h3 style="margin-top : 20px;">Début janvier</h3>
<ul>
    <li>Personnes âgées séjournant dans un établissement collectif (type EHPAD) ;</li>
    <li>Personnel soignant, pompiers, aides à domicile (plus de 50 ans) ;</li>
    <li>Personnes en situation de handicap vulnérables qui sont hébergées en maison d'accueil spécialisées (MAS) et en
        foyer d'accueil médicalisé (FAM).
    </li>
</ul>

<h3 style="margin-top : 50px;">18 janvier</h3>
<ul>
    <li>Personnes âgées de plus de 75 ans en priorité ; puis personnes âgées de plus de 65 ans dans un second temps,
    </li>
</ul>

<h3 style="margin-top : 50px;">Début février</h3>
<ul>
    <li>Tous les professionnels de Santé. 
    </li>
</ul>

<h3 style="margin-top : 50px;">31 mars</h3>
<ul>
    <li>Personnes âgées de plus de 70 ans,</li>
    <li>Personnes à risque de plus de 50 ans.</li>
</ul>

<h3 style="margin-top : 50px;">16 avril</h3>
<ul>
    <li>Personnes âgées de plus de 60 ans,</li>
    <li>Les enseignants qui le désirent.</li>
</ul>

<h3 style="margin-top : 50px;">15 mai</h3>
<ul>
    <li>Personnes de 50 ans et plus.
    </li>
</ul>

<h3 style="margin-top : 50px;">Été</h3>
<ul>
    <li>Ensemble de la population.</li>
</ul>
